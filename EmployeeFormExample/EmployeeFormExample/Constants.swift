import Foundation
import SwiftUI

/// A collection of Constants that refer to system symbols (SF Symbols) that can be used as placeholder images for consistency throughout the app.
public enum Constants {
    /// A placeholder image that signifies an image portrait.
    public static let personPlaceholder = "person.circle.fill"
    /// A placeholder image that signifies a generic photo.
    public static let photoPlaceholder = "photo.circle.fill"
    /// A placeholder image that signifies an error. Errors may occur when attempting to load a selected image.
    public static let errorPlaceholder = "exclamationmark.circle.fill"
}
